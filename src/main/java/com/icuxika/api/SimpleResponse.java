package com.icuxika.api;

public class SimpleResponse {

    private Integer code;

    private String msg;

    public SimpleResponse() {
    }

    public Integer getCode() {
        return code;
    }

    public void setCode(Integer code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
